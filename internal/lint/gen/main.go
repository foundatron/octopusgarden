// Command gen produces schemas/spec-format.md and schemas/scenario-format.md
// from the lint rule registry.
package main

import (
	"errors"
	"fmt"
	"os"
	"path/filepath"
	"text/template"

	"github.com/foundatron/octopusgarden/internal/lint"
)

var errGoModNotFound = errors.New("go.mod not found")

func main() {
	if err := run(); err != nil {
		fmt.Fprintf(os.Stderr, "gen: %v\n", err)
		os.Exit(1)
	}
}

func run() error {
	root, err := repoRoot()
	if err != nil {
		return err
	}

	if err := generate(
		filepath.Join(root, "schemas", "spec-format.md"),
		specTmpl,
		lint.SpecRules,
	); err != nil {
		return fmt.Errorf("spec-format.md: %w", err)
	}

	if err := generate(
		filepath.Join(root, "schemas", "scenario-format.md"),
		scenarioTmpl,
		lint.ScenarioRules,
	); err != nil {
		return fmt.Errorf("scenario-format.md: %w", err)
	}

	return nil
}

func generate(path, tmplText string, rules []lint.Rule) error {
	tmpl, err := template.New("doc").Parse(tmplText)
	if err != nil {
		return fmt.Errorf("parse template: %w", err)
	}

	data := struct {
		Errors   []lint.Rule
		Warnings []lint.Rule
	}{}

	for _, r := range rules {
		switch r.Level {
		case lint.Error:
			data.Errors = append(data.Errors, r)
		case lint.Warning:
			data.Warnings = append(data.Warnings, r)
		}
	}

	f, err := os.Create(path)
	if err != nil {
		return fmt.Errorf("create %s: %w", path, err)
	}

	execErr := tmpl.Execute(f, data)
	closeErr := f.Close()

	if execErr != nil {
		return fmt.Errorf("execute template: %w", execErr)
	}
	return closeErr
}

// repoRoot returns the repository root by walking up from cwd to find go.mod.
func repoRoot() (string, error) {
	dir, err := os.Getwd()
	if err != nil {
		return "", fmt.Errorf("getwd: %w", err)
	}
	for {
		if _, err := os.Stat(filepath.Join(dir, "go.mod")); err == nil {
			return dir, nil
		}
		parent := filepath.Dir(dir)
		if parent == dir {
			return "", errGoModNotFound
		}
		dir = parent
	}
}

var specTmpl = `# Spec Format Reference

<!-- Code generated by go generate; DO NOT EDIT. -->

Specs are markdown files that describe the desired behavior of the system under test.
The attractor loop uses the spec as its sole input for code generation.

## MUST (Errors)

Violations prevent the spec from being used.

| ID | Rule |
|----|------|
{{- range .Errors}}
| {{.ID}} | {{.Summary}}{{with .Detail}} — {{.}}{{end}} |
{{- end}}

## SHOULD (Warnings)

Violations are reported but do not block usage.

| ID | Rule |
|----|------|
{{- range .Warnings}}
| {{.ID}} | {{.Summary}}{{with .Detail}} — {{.}}{{end}} |
{{- end}}

## Metadata

Optional key-value fields can appear in the description text (between the title heading and the
next section heading). They use the format ` + "`Key: value`" + ` on their own line.

| Key | Description |
|-----|-------------|
| ` + "`Test-Command`" + ` | Shell command run inside the HTTP container after a successful health check. A non-zero exit code is treated as a mechanical failure (like a build failure) and triggers another generation iteration. The command runs via ` + "`sh -c`" + ` with a 60-second timeout. Example: ` + "`Test-Command: go test ./...`" + ` |

## Notes

- Headings inside fenced code blocks (` + "```" + ` or ` + "~~~" + `) are ignored by the parser.
- The spec content is passed as a string to the attractor — file paths are never exposed.
- Only ATX-style headings (` + "`# Title`" + `) are recognized; setext-style is not supported.
- ` + "`Test-Command`" + ` requires an HTTP container (i.e. scenarios with HTTP request steps). It has no effect when only exec-only or gRPC containers are started.
`

var scenarioTmpl = `# Scenario Format Reference

<!-- Code generated by go generate; DO NOT EDIT. -->

Scenarios are YAML files that describe HTTP-based test cases for validating generated code.
They are holdout data — the attractor loop never sees them during code generation.

## MUST (Errors)

Violations prevent the scenario from being loaded.

| ID | Rule |
|----|------|
{{- range .Errors}}
| {{.ID}} | {{.Summary}}{{with .Detail}} — {{.}}{{end}} |
{{- end}}

## SHOULD (Warnings)

Violations are reported but do not block loading.

| ID | Rule |
|----|------|
{{- range .Warnings}}
| {{.ID}} | {{.Summary}}{{with .Detail}} — {{.}}{{end}} |
{{- end}}

## Notes

- Setup steps run before judged steps; failures in setup are fatal (score 0).
- Setup steps do not require ` + "`expect`" + ` or ` + "`description`" + ` fields.
- Variable capture uses ` + "`{name}`" + ` syntax in paths, headers, and body strings.
- JSONPath uses dot-notation only: ` + "`$.field.sub`" + ` (no array indexing or wildcards).
- The runner limits response body reads to 10 MB.
- The judge model scores satisfaction 0-100 per scenario via LLM-as-judge.
- Duplicate scenario IDs are checked across all files in a directory.
`
